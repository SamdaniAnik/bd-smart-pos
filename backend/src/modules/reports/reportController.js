async function buildStockValuationPayload(branchId, query = {}) {
  const asOf = query.asOf ? new Date(`${query.asOf}T23:59:59.999Z`) : null;
  const category = String(query.category || "").trim();
  const warehouseId = query.warehouseId ? Number(query.warehouseId) : null;

  const products = await prisma.product.findMany({
    where: {
      branchId,
      ...(category ? { category: { contains: category } } : {}),
    },
    select: {
      id: true,
      name: true,
      category: true,
      stock: true,
      stockKg: true,
      sellByWeight: true,
      price: true,
      unitPrice: true,
    },
    orderBy: { name: "asc" },
    take: 5000,
  });
  const productIds = products.map((p) => p.id);
  const landedByProduct = await getLatestLandedCostByProduct(branchId);
  if (!productIds.length) {
    return { totalValue: 0, asOf: asOf ? asOf.toISOString().slice(0, 10) : null, category, warehouseId, rows: [] };
  }

  const shouldUseLedger = Boolean(asOf || warehouseId);
  if (!shouldUseLedger) {
    const rows = products.map((p) => {
      const landedInfo = landedByProduct.get(Number(p.id)) || null;
      const baseUnitCost = Number(landedInfo?.baseUnitCost || p.unitPrice || p.price || 0);
      const landedUnitCost = Number(landedInfo?.landedUnitCost || baseUnitCost);
      const unitCost = landedUnitCost;
      const sellingPrice = Number(p.price || 0);
      const baseMarginPct = calcMarginPct(sellingPrice, baseUnitCost);
      const landedMarginPct = calcMarginPct(sellingPrice, landedUnitCost);
      const profitMargin = landedMarginPct;
      const stockQty = p.sellByWeight ? Number(p.stockKg || 0) : Number(p.stock || 0);
      return {
        productId: p.id,
        name: p.name,
        category: p.category || "",
        stock: stockQty,
        stockDisplay: formatProductStock(p),
        saleUnit: p.sellByWeight ? "KG" : "PCS",
        sellByWeight: Boolean(p.sellByWeight),
        unitCost,
        baseUnitCost: Number(baseUnitCost.toFixed(4)),
        landedUnitCost: Number(landedUnitCost.toFixed(4)),
        sellingPrice: Number(sellingPrice.toFixed(2)),
        profitMargin: Number(profitMargin.toFixed(2)),
        baseMarginPct: Number(baseMarginPct.toFixed(2)),
        landedMarginPct: Number(landedMarginPct.toFixed(2)),
        marginImpactPct: Number((landedMarginPct - baseMarginPct).toFixed(2)),
        valuation: Number((stockQty * unitCost).toFixed(2)),
      };
    });
    const totalValue = rows.reduce((sum, r) => sum + Number(r.valuation || 0), 0);
    return {
      totalValue: Number(totalValue.toFixed(2)),
      asOf: null,
      category,
      warehouseId: null,
      rows,
    };
  }

  const ledgers = await prisma.stockLedger.findMany({
    where: {
      branchId,
      productId: { in: productIds },
      ...(warehouseId ? { warehouseId } : {}),
      ...(asOf ? { createdAt: { lte: asOf } } : {}),
    },
    select: { productId: true, inQty: true, outQty: true, unitCost: true },
    take: 50000,
  });
  const agg = new Map();
  for (const row of ledgers) {
    const pid = Number(row.productId);
    if (!agg.has(pid)) agg.set(pid, { qty: 0, value: 0 });
    const current = agg.get(pid);
    const inQty = Number(row.inQty || 0);
    const outQty = Number(row.outQty || 0);
    const unitCost = Number(row.unitCost || 0);
    current.qty += inQty - outQty;
    current.value += inQty * unitCost - outQty * unitCost;
    agg.set(pid, current);
  }

  const rows = products.map((p) => {
    const landedInfo = landedByProduct.get(Number(p.id)) || null;
    const baseUnitCost = Number(landedInfo?.baseUnitCost || p.unitPrice || p.price || 0);
    const landedUnitCost = Number(landedInfo?.landedUnitCost || baseUnitCost);
    const current = agg.get(p.id) || { qty: 0, value: 0 };
    const qty = Number(current.qty || 0);
    const value = Number(current.value || 0);
    const unitCost = qty > 0 ? value / qty : landedUnitCost;
    const sellingPrice = Number(p.price || 0);
    const baseMarginPct = calcMarginPct(sellingPrice, baseUnitCost);
    const landedMarginPct = calcMarginPct(sellingPrice, unitCost);
    const profitMargin = landedMarginPct;
    const valuation = qty > 0 ? value : 0;
    const stockRow = { ...p, stock: qty, stockKg: p.sellByWeight ? qty : p.stockKg };
    return {
      productId: p.id,
      name: p.name,
      category: p.category || "",
      stock: Number(qty.toFixed(2)),
      stockDisplay: formatProductStock(stockRow),
      saleUnit: p.saleUnit || p.unitOfMeasure || null,
      sellByWeight: Boolean(p.sellByWeight),
      unitCost: Number(unitCost.toFixed(2)),
      baseUnitCost: Number(baseUnitCost.toFixed(4)),
      landedUnitCost: Number(unitCost.toFixed(4)),
      sellingPrice: Number(sellingPrice.toFixed(2)),
      profitMargin: Number(profitMargin.toFixed(2)),
      baseMarginPct: Number(baseMarginPct.toFixed(2)),
      landedMarginPct: Number(landedMarginPct.toFixed(2)),
      marginImpactPct: Number((landedMarginPct - baseMarginPct).toFixed(2)),
      valuation: Number(valuation.toFixed(2)),
    };
  });
  const totalValue = rows.reduce((sum, r) => sum + Number(r.valuation || 0), 0);
  return {
    totalValue: Number(totalValue.toFixed(2)),
    asOf: asOf ? asOf.toISOString().slice(0, 10) : null,
    category,
    warehouseId: warehouseId || null,
    rows,
  };
}

