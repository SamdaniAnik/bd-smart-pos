/** @param {string} lang */
export function t(lang, key, params) {
  let s = dictionary[lang]?.[key] ?? dictionary.en[key] ?? key;
  if (params != null && typeof s === "string") {
    for (const [k, v] of Object.entries(params)) {
      const needle = `{${k}}`;
      const repl = String(v);
      if (typeof s.replaceAll === "function") {
        s = s.replaceAll(needle, repl);
      } else {
        s = s.split(needle).join(repl);
      }
    }
  }
  return s;
}

export function getLang() {
  if (typeof window === "undefined") return "en";
  return localStorage.getItem("bd_pos_lang") === "bn" ? "bn" : "en";
}
