export function tagsToText(tags) {
  if (!Array.isArray(tags)) return "";
  return tags.map((x) => String(x || "").trim()).filter(Boolean).join(", ");
}

export function textToTags(text) {
  return String(text || "")
    .split(/[,\n]/)
    .map((x) => x.trim())
    .filter(Boolean)
    .slice(0, 30);
}

