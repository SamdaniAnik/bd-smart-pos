    halalCertNo: form.halalCertNo || null,
    importerName: form.importerName || null,
    importerAddress: form.importerAddress || null,
    productCondition: form.productCondition || null,
    purchaseUnit: form.purchaseUnit || null,
    unitsPerPack: form.unitsPerPack !== "" && form.unitsPerPack != null ? Number(form.unitsPerPack) : null,
