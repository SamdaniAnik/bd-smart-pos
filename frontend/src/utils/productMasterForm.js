    unitOfMeasure: form.saleUnit || form.unitOfMeasure || "PCS",
    tags: textToTags(form.tagsText),
    isActive: Boolean(form.isActive),
    unitPrice: Number(form.unitPrice || 0),
    price: Number(form.price),
    mrp: Number(form.mrp || 0),
    hsCode: form.hsCode || null,
    stock: Number(form.stock),
    size: form.size || null,
    color: form.color || null,
    specification: form.specification || null,
    attributeValues: attributeDraft || {},
    imageUrl: form.imageUrl || null,
    imageGallery,
    vatRate: Number(form.vatRate || 0),
    reorderLevel: Number(form.reorderLevel || 0),
    defaultDiscountType: form.defaultDiscountType || null,
    defaultDiscountValue: Number(form.defaultDiscountValue || 0),
    batchTracked: Boolean(form.batchTracked),
    trackExpiry: Boolean(form.trackExpiry),
    trackSerial: Boolean(form.trackSerial),
    trackImei: Boolean(form.trackImei),
    requiresKyc: Boolean(form.requiresKyc),
    warrantyDays: form.warrantyDays !== "" && form.warrantyDays != null ? Number(form.warrantyDays) : null,
    isRawMaterial: Boolean(form.isRawMaterial),
    isManufactured: Boolean(form.isManufactured),
    sellByWeight: Boolean(form.sellByWeight),
    stockKg: Number(form.stockKg || 0),
    hasVariants: Boolean(form.hasVariants),
    weightGrams: form.weightGrams !== "" ? Number(form.weightGrams) : null,
    shelfLifeDays: form.shelfLifeDays !== "" ? Number(form.shelfLifeDays) : null,
    storageCondition: form.storageCondition || null,
    genericName: form.genericName || null,
    strength: form.strength || null,
    dosageForm: form.dosageForm || null,
    drugRegNo: form.drugRegNo || null,
    internalNotes: form.internalNotes || null,
    sdRate: Number(form.sdRate || 0),
    nbrProductCode: form.nbrProductCode || null,
    bstiCertNo: form.bstiCertNo || null,
